class Calculadora:
    def adicao(self,n1,n2):
        print(n1+n2)
    
    def subtracao(self,n1,n2):
        print(n1-n2)
    
    def multiplicacao(self,n1,n2):
        print(n1*n2)
    
    def divisao(self,n1,n2):
        print(n1/n2)

c1 = Calculadora()
c1.adicao(6,3)
c1.subtracao(6,3)
c1.multiplicacao(6,3)
c1.divisao(6,3)
